function cacher() {

    var x = document.getElementById('mybutton');
    if (x.style.display === 'none') {
        x.style.display = '';
        document.getElementById('btn btn-info').innerHTML = '-';
    } else {
        x.style.display = 'none';
        document.getElementById('btn btn-info').innerHTML = '+';

    }
}

